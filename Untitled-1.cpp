#include <iostream>

int main() {
    // Declaración de variables
    int numeroEntero;
    double numeroDecimal;
    char caracter;
    bool valorLogico;

    // Asignación de valores
    numeroEntero = 10;
    numeroDecimal = 9.8;
    caracter = 'A';
    valorLogico = true;

    // Imprimir valores
    std::cout << "Entero: " << numeroEntero << std::endl;
    std::cout << "Decimal: " << numeroDecimal << std::endl;
    std::cout << "Caracter: " << caracter << std::endl;
    std::cout << "Logico: " << valorLogico << std::endl;

    return 0;
}
